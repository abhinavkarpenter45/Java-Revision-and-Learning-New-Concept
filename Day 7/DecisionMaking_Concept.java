/*
Topic - Control Statements (Decision Making Statement)
Ans: Control Statement is use to control flow of program by executing different 
     block of code based on condition.

Types of Decision Making Statements
1. if -> executes a block of code if the condition is true.
2. if-else -> execute if block if condition is true other wise execute else block.
3. if-else-if ladder -> check multiple conditions and execute first matching block.
4. Nasted - if -> if inside another if statements.
5. Switch Statement -> Select one matching in multiple options based on variable or expressions.

Jump Statements: it is used indside of Decision making statements
1. break -> condition match then stop program
2. continue -> skip current match value 
*/