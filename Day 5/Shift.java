public class Shift {
    public static void main(String[] args) {
        
        int a = 10;
        int b = 10;
        int c = - 10;

        System.out.println(a << 1);
        System.out.println(a << 2);

        System.out.println(b >> 1);
        System.out.println(b >> 2);

        System.out.println(c >>> 1);
    }
}
